--Câu 1: Tạo quan hệ và khai báo tất cả các ràng buộc khóa chính, khóa ngoại. Thêm vào 3 thuộc tính 
--GHICHU, DIEMTB, XEPLOAI cho quan hệ HOCVIEN.

CREATE TABLE KHOA(
	MAKHOA varchar(4),
	TENKHOA nvarchar(40),
	NGTLAP smalldatetime,
	TRGKHOA char(4),
	primary key (MAKHOA) --khoá chính
	)
CREATE TABLE MONHOC(
	MAMH varchar(10),
	TENMH nvarchar(40),
	TCLT tinyint,
	TCTH tinyint,
	MAKHOA varchar(4),
	primary key (MAMH)  --khoá chính
	)
CREATE TABLE DIEUKIEN(
	MAMH varchar(10), --thuộc tính khóa
	MAMH_TRUOC varchar(10), --thuộc tính khóa
	)
CREATE TABLE GIAOVIEN(
	MAGV char(4),
	HOTEN char(40),
	HOCVI varchar(10),
	HOCHAM varchar(10),
	GIOITINH varchar(3),
	NGAYSINH smalldatetime,
	NGVL smalldatetime,
	HESO numeric(4,2),
	MUCLUONG money,
	MAKHOA varchar(4),
	primary key (MAGV)  --khoá chính
	)
CREATE TABLE LOP(
	MALOP char(3),
	TENLOP nvarchar(40),
	TRGLOP char(5),
	SISO tinyint,
	MAGVCN char(4),
	primary key (MALOP)  --khoá chính
	)

CREATE TABLE HOCVIEN(
	MAHV char(5),
	HO nvarchar(40),
	TEN nvarchar(10),
	NGSINH smalldatetime,
	GIOITINH varchar(3),
	NOISINH nvarchar(40),
	MALOP char(3),
	primary key (MAHV)  --khoá chính
	)

CREATE TABLE GIANGDAY(
	MALOP char(3), --thuộc tính khóa
	MAMH varchar(10), --thuộc tính khóa
	MAGV char(4),
	HOCKY tinyint,
	NAM smallint,
	TUNGAY smalldatetime,
	DENNGAY smalldatetime,
	)
CREATE TABLE KETQUATHI(
	MAHV char(5), --thuộc tính khóa
	MAMH varchar(10), --thuộc tính khóa
	LANTHI tinyint, --thuộc tính khóa
	NGATHI smalldatetime,
	DIEM numeric(4,2),
	KQUA varchar(10),
	)

ALTER TABLE KHOA
ADD constraint FK_KHOA_TRGKHOA foreign key (TRGKHOA) references GIAOVIEN(MAGV)

ALTER TABLE MONHOC
